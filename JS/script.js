function spawnFallingBread(e) {
    const ovenLevel = (gameState.upgrades.oven_1.count || 0)
                    + (gameState.upgrades.oven_2.count || 0)
                    + (gameState.upgrades.oven_3.count || 0)
                    + (gameState.upgrades.oven_4.count || 0)
                    + (gameState.upgrades.oven_5.count || 0);
    const minCount = 3 + ovenLevel;
    const maxCount = 6 + ovenLevel * 2;
    const count = minCount + Math.floor(Math.random() * (maxCount - minCount + 1));
    for (let i = 0; i < count; i++) {
        const img = document.createElement('img');
        const idx = 1 + Math.floor(Math.random() * 8);
        img.src = `assets/images/bread${idx}.png`;
        img.classList.add('falling-bread');
        const rect = clickerBtn.getBoundingClientRect();
        const cx = rect.left + rect.width / 2;
        const cy = rect.top + rect.height / 2;
        img.style.left = `${cx + (Math.random() - 0.5) * 120}px`;
        img.style.top = `${cy + (Math.random() - 0.5) * 60}px`;
        img.style.width = `${40 + Math.random() * 40}px`;
        img.style.height = `${40 + Math.random() * 40}px`;
        img.style.animationDuration = `${1.2 + Math.random() * 1.2}s`;
        img.style.animationDelay = `${Math.random() * 0.3}s`;
        document.body.appendChild(img);
        setTimeout(() => img.remove(), 3000);
    }
}

