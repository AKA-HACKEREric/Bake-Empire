function generateShop() {
    const container = document.getElementById('shop-items-container');
    container.innerHTML = '';
    const lang = i18nData[currentLang];
    
    const categories = [
        { key: 'tools', label: lang.shopCatTools || 'Tools' },
        { key: 'staff', label: lang.shopCatStaff || 'Staff' },
        { key: 'equipment', label: lang.shopCatEquipment || 'Equipment' }
    ];
    
    for (const cat of categories) {
        const catItems = Object.entries(upgradeDefs).filter(([k, d]) => d.category === cat.key);
        if (catItems.length === 0) continue;
        
        const header = document.createElement('div');
        header.className = 'shop-category-header';
        header.textContent = cat.label;
        container.appendChild(header);
        
        for (const [key, def] of catItems) {
            const itemEl = document.createElement('div');
            itemEl.className = 'upgrade-item';
            itemEl.id = def.id;
            
            const iconHtml = def.icon ? `<img class="upg-icon" src="${def.icon}" alt="">` : '';
            
            itemEl.innerHTML = `
                ${iconHtml}
                <div class="upgrade-info">
                    <h3 class="upg-name"></h3>
                    <p>+<span class="upg-val">${formatNumber(def.value)}</span><span class="upg-desc"></span> (<span class="upg-owned-label"></span><span class="count">0</span>)</p>
                </div>
                <button class="buy-btn"><span class="upg-cost-label"></span><span class="cost"></span></button>
            `;
            
            const btn = itemEl.querySelector('.buy-btn');
            btn.addEventListener('click', () => {
                const currentData = gameState.upgrades[key];
                if (gameState.score >= currentData.cost) {
                    if (cashSound) {
                        const s = cashSound.cloneNode();
                        s.volume = parseInt(volumeSlider.value) / 100;
                        s.play().catch(() => {});
                    }
                    gameState.score -= currentData.cost;
                    if (def.type === 'click') gameState.clickPower += def.value;
                    else if (def.type === 'auto') gameState.pointsPerSecond += def.value;
                    currentData.count += 1;
                    currentData.cost = Math.floor(currentData.cost * def.costMultiplier);
                    updateDisplay();
                    if (key.startsWith('oven_')) updateClickerImage();
                    saveGame();
                }
            });
            container.appendChild(itemEl);
        }
    }
}

